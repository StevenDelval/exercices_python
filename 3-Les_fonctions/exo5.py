def moyenne(nb1,nb2,nb3):
    '''Moyenne de trois nombres.\n
        Param:
            nb1: (number) un nombre
            nb2: (number) un nombre
            nb3: (number) un nombre
        Return:
            (float) moyenne des trois nombre
    
    '''
    somme= nb1+nb2+nb3
    return somme/3

print(moyenne(10,13,10))